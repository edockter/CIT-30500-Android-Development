public class Tag {
    public String Tag;
    public String Title;
    public int Usage;

    public String getTag() {
        return Tag;
    }

    public void setTag(String tag) {
        Tag = tag;
    }

    public String getTitle() {
        return Title;
    }

    public void setTitle(String title) {
        Title = title;
    }

    public int getUsage() {
        return Usage;
    }

    public void setUsage(int usage) {
        Usage = usage;
    }
}
